import React from 'react';
import Chip from '@material-ui/core/Chip';
import List from '@material-ui/core/List';
import { makeStyles } from '@material-ui/core/styles';


const useStyles = makeStyles({
  chip: {
    margin: '4px'
  }
});

export default function TagArray({allTags}) {
  const classes = useStyles();
  const [chipVariant, changeVariant] = React.useState('outlined');

  // const handleFilter = (tagToFilter) => () => {
  //   setFilterTag((tags) => tags.filter((chip) => chip.key !== tagToFilter.key));
  // };

   const setVariant = () => {
     if(chipVariant === 'outlined'  ) {
      changeVariant('default');
     } else {
      changeVariant('outlined');
     }
  };


  return (
    <List component="nav">
      {allTags.map((tagLabel, index) => (

        <Chip
        size="small"
        key={index}
        variant={chipVariant}
        color="primary"
        className={classes.chip}
        label={tagLabel}
        clickable={true}
        onClick={() => { setVariant() }}
        />
        ))}
    </List>
  )

}



































































































































































































































































































































































































































































































































































































